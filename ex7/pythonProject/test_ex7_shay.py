def test_compare_2d_lists():
    assert compare_2d_lists([[2]], [[3, 3]]) == False
    assert compare_2d_lists([], []) == True
    assert compare_2d_lists([], [[3]]) == False
    assert compare_2d_lists([[2]], []) == False
    assert compare_2d_lists([[2, 2]], [[3]]) == False
    assert compare_2d_lists([[2]], [[3, 2]]) == False
    assert compare_2d_lists([[2]], [[3], [3, 2]]) == False
    assert compare_2d_lists([[2,2],[2]], [[3], [3, 2]]) == False
    assert compare_2d_lists([[2], [3, 2]], [[3], [3, 2]]) == False
    assert compare_2d_lists([[3], [3, 2]], [[3], [3, 2]]) == True
    assert compare_2d_lists([[2], [3, 2], [], [1, 2, 3, 4, 4, 1, -5]],
                            [[2], [3, 2], [], [1, 2, 3, 4, 4, 1, -5]]) == True
    assert compare_2d_lists([[3], [3, 1]], [[3], [3, 1]]) == True
    assert compare_2d_lists([[3], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]],
                            [[3], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]]) == True
    assert compare_2d_lists([[1], [0, 0]], [[3], [3, 2]]) == False
    assert compare_2d_lists([[3], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]],
                            [[3], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4]]) == False
    assert compare_2d_lists([[3, 2], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]],
                            [[3], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]]) == False
    assert compare_2d_lists([[3], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]],
                            [[3, 2], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]]) == False
    assert compare_2d_lists([[9, 3], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4, 5]],
                            [[3, 9], [3, 2], [], [912498, -15135, 0.5, 1.1248795, -1254987], [1, 2, 3, 4]]) == False
    assert compare_2d_lists([[1]], [[3]]) == False


